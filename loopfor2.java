// example of for loop.
public class loopfor2 {
  public static void main(String[] args)
  {
   
   for(int i = 1; i <=10; i++)
      { int s = 0;
		s = s+i;
		System.out.println("The Result is:="+s);
   }
   
   System.out.println("Exit loop when the test condition is false. That is i = 11");
  }
}